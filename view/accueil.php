            <h3>La Ligne 1</h3>
            <p>La ligne 1 est livrée par la Ville de Paris à son exploitant, la Compagnie du chemin de fer métropolitain de Paris, le 15 juin 1900, qui y fait circuler ses rames pour l'essai de la ligne et l'instruction du personnel. La ligne est mise en service le 19 juillet 1900.En 1901, une seconde concession est accordée par la Ville de Paris à l'ingénieur Jean-Baptiste Berlier. La Société du chemin de fer électrique souterrain Nord-Sud de Paris est créée et se substitue à lui pour réaliser une nouvelle ligne de métro (Porte de Versailles - Jules Joffrin).</p>
