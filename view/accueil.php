            <h3 style="margin-top: 15px;">Premiers autobus</h3>
            <p>Le salon de l'automobile de Paris en 1905 présente au public un nouveau véhicule : l'autobus.La première ligne complète de bus parisienne est inaugurée le 11 juin 1906 et relie Montmartre à Saint-Germain-des-Prés (ligne AM), sur un parcours de 5,8 kilomètres. Le bus choisi par la Compagnie générale des omnibus (CGO) est le Schneider Brillié P2, à la suite d'un concours et d'une présentation de neuf modèles lors du premier salon de l'automobile de 1905, qui se déroule au Grand Palais.</p>
